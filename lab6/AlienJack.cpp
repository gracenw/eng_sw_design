void AlienJack :: update(double xacc) {
    if(right && x < 115) {
                x = x + 1;
                uLCD.BLIT((x - 1), y, ALIEN_WIDTH, ALIEN_HEIGHT, backfillJ);
            }
            else if(right && x >= 115) {
                x = x - 1;
                right = false;
                uLCD.BLIT((x + 1), y, ALIEN_WIDTH, ALIEN_HEIGHT, backfillJ);
            }   
            else if(!right && x > 0) {
                x = x - 1;
                uLCD.BLIT((x + 1), y, ALIEN_WIDTH, ALIEN_HEIGHT, backfillJ);
            }
            else if(!right && x <= 0) {
                x = x + 1;
                right = true;
                uLCD.BLIT((x - 1), y, ALIEN_WIDTH, ALIEN_HEIGHT, backfillJ);
            }
        }